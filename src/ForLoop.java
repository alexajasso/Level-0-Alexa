 // create a for loop class
// using a for loop, print numbers 1-10
// using a for loop, print numbers 75-50

public class ForLoop {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		for (int i = 12; i >= 0; i--) {
			System.out.println(i);
		}
	}
}